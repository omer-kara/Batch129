package day10switchloops;

public class Loops01Tekrar {
    public static void main(String[] args) {

        for (int i = 1; i < 6; i++) {

            System.out.println("Hi!");


        }
        for (int i = 11; i < 45; i++) {

            System.out.println(i);
        }
        for (int i = 40; i > 22; i--) {
            if (i % 2 == 0) {

                System.out.println(i);
            }
        }

        for(int i =18   ;  i<57   ; i++){
            if(i%2!=0){
                System.out.println(i);
            }

        }
    }
}
