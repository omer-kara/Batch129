package day13whileloopwhileloop;

public class DoWhileLopp01 {

    public static void main(String[] args) {

        //1)While - loop

        // 1-while body hic calismadi.
        // 2-while - loop Zero Execution mumkundur.

        int i=1;

        while (i<1){
            System.out.println("Sen bir while loopsun");
            i++;
        }

        //2) do while loop
        // (do while )java ya sart koymadan yap demek

        //1-do-while body calisti.
        //2-do-while kullandiginizda loop body en az bir kere calisisr.
        //do-while de "zero execution" (Sifir yurutme) mumkun degildir.

        int k=1;

        do{
            System.out.println("Sen bir do while loopsun");
            k++;

        }while (k<1);

    }

}
