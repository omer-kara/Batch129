package day11forloop;

public class ForLoop05Tekrar {
    public static void main(String[] args) {

        for (int i= 1 ; i<5 ; i++){

            System.out.println("Week : " + i);

            for (int k = 1 ; k<8 ; k++){

                System.out.println("day : " + k);
            }
        }

    }
}
